/**
 * Get an icon by name
 * @param {string} name - Icon name
 * @param {string} [className] - Additional CSS classes
 * @returns {string} SVG markup
 */
export function getIcon(name: string, className: string = ''): string {
    const icon = ICONS[name];
    if (!icon) return '';

    if (className) {
        return icon.replace('class="hud-icon', `class="hud-icon ${className}`);
    }
    return icon;
}

/**
 * Replace an element's emoji content with an SVG icon
 * @param {HTMLElement} element
 * @param {string} iconName
 */
export function replaceWithIcon(element: HTMLElement, iconName: string): void {
    const icon = ICONS[iconName];
    if (icon && element) {
        element.innerHTML = icon;
    }
}
